package com.a286;

public class ArithmaticOpEx 
{
public static void main(String[] args) {
	
	int num1 = 8;
	int num2 = 2;
	
	int add,sab,mul,div,mod;
	
	add = num1+num2;
	sab = num1-num2;
	mul = num1*num2;
	div = num1/num2;
	mod = num1%num2;
	
	System.out.println("Addition of two value is : "+add);
	System.out.println("Substractions of two value is : "+sab);
	System.out.println("Multiplication of two value is : "+mul);
	System.out.println("Division of two value is : "+div);
	System.out.println("Modulus of two value is : "+mod);
	
}
}
